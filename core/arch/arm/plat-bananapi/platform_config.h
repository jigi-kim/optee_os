#ifndef PLATFORM_CONFIG_H
#define PLATFORM_CONFIG_H

/* TODO: Make sure the value below is same with the data cache line length */
#define STACK_ALIGNMENT		64

/* GIC */
#define GIC_BASE		0x01c80000
#define GICC_OFFSET		0x00002000
#define GICD_OFFSET		0x00001000

#define GIC_REG_SIZE		0x00008000

/* Console */
#define UART0_BASE		0x01c28000
#define UART7_BASE		0x01c29c00

#define UART_REG_SIZE		0x00000400

#define CONSOLE_UART_BASE	UART0_BASE
#define CONSOLE_UART_CLK_IN_HZ	115200
#define CONSOLE_BAUDRATE	19200000

/* Memory Allocation */
#define DRAM0_BASE		0x40000000
#define DRAM0_SIZE		0x80000000

#define DRAM0_BASE_NSEC		0x48000000
#define DRAM0_SIZE_NSEC		0x78000000

#define CFG_SHMEM_START		DRAM0_BASE
#define CFG_SHMEM_SIZE		0x00400000

#define TZDRAM_BASE		0x42000000
#define TZDRAM_SIZE		0x04000000

/* TEE */
#define CFG_TEE_CORE_NB_CORE	1
#define CFG_TEE_RAM_VA_SIZE	0x00400000

#define CFG_TEE_LOAD_ADDR	TZDRAM_BASE

#define CFG_TEE_RAM_START	TZDRAM_BASE
#define CFG_TEE_RAM_PH_SIZE	CFG_TEE_RAM_VA_SIZE

#define CFG_TA_RAM_START	ROUNDUP((TZDRAM_BASE + CFG_TEE_RAM_VA_SIZE), \
					CORE_MMU_DEVICE_SIZE)
#define CFG_TA_RAM_SIZE		0x01000000

#endif
